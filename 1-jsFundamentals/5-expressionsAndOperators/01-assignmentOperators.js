let x = 20;

//addition assignment operator
x += 1; // x = x - 1
console.log(x);

//mulitiplication assignment operator
x *= 2; //x = x * 2
console.log(x);

//division assignment operator
x /= 4; //x = x / 4
console.log(x);

//remainder (modulus) assignement operator
x %= 2 
console.log(x);

//modulus operator
console.log(5%3); //5/3 -> 1 2/3 -> 2
console.log(25%7); //25/7 -> 3 4/7 -> 4

//exponental assignemt operator
x **= 2;
console.log(x);

let y = 7;
y **= 3;
console.log(y);